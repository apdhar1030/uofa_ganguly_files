  180 CONTINUE
  200 CONTINUE
      GO TO 214
  210 J = J - 1
      WRITE (ISYSWR, 490)  I,J
  214 CALL INTOEX(X)
      CALL VERMIN(V,MAXINT,MAXINT,NPAR,IFAIL)
      IF (IFAIL .LT. 1)  GO TO 222
      WRITE (ISYSWR,520)
