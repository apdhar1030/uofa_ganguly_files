      DIMENSION SPT(3,MAXSPT) , ALINE(100), SAV(3)
      DIMENSION XVALUS(12)
      DATA DOT,SLASH,ABLANK/1H.,1H/,1H /
      DATA MAXNX,MAXNY/ 98, 56/
C      WRITE(IVIDEO,*)'@@@@@@ ENTRA IN PLTCON...NSPT',NSPT
      IF (NSPT .LE. 1)  RETURN
      XBEST = SPT(1,1)
      YBEST = SPT(2,1)
      CHBEST = SPT(3,1)
