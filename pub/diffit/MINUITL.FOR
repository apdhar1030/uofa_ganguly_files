         SUBROUTINE  MIGRAD
C      OVERLAY OVMIGRAD
CC        PERFORMS A LOCAL FUNCTION MINIMIZATION USING BASICALLY THE
CC        METHOD OF DAVIDON-FLETCHER-POWELL AS MODIFIED BY FLETCHER
CC        REF. -- FLETCHER, COMP.J. 13,317 (1970)   'SWITCHING METHOD'
CC
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C********************************************************************
C********** C O M M O N     PER  MINUITL ****************************
C*********************************************************************
C********************************************************************
C********** C O M M O N     PER  MINUITL ****************************
      COMMON
     1/MINERR/ ERP(150)     ,ERN(150)
     2/PARINT/ X(55)       ,XT(55)     ,DIRIN(55)  ,MAXINT     ,NPAR
     3/PAREXT/ U(150)       ,FNAM(150)    ,WERR(150)   ,MAXEXT     ,NU
     4/LIMITS/ ALIM(150),BLIM(150),LCODE(150),LCORSP(150) ,LIMSET
     5/VARIAN/ V(55,55)
     7 /FIX/    XS(55),   XTS(55),   WTS(55),   IPFIX(55),   NPFIX
     C/CASC/ JH, JL, Y(56)
     F/DERIVA/ G(150)       ,G2(150)
     G/SIMVEC/ P(55,56)    ,PSTAR(55)  ,PSTST(55)  ,PBAR(55)   ,PRHO(55)
     J/VARIAT/ VT(55,55)
      COMMON
     6/UNIT/  ISYSRD,ISYSWR,ISYSPU,IVIDEO
     8/TITLE/ TITLE(13),DATE(2),ISW(7),NBLOCK
     9/CONVER/ EPSI,APSI,VTEST,NSTEPQ,NFCN,NFCNMX
     A/CARD/ CWORD,CWORD2,CWORD3,WORD7(7)
     B/MINIMA/ AMIN,UP,NEWMIN,ITAUR,SIGMA
C*********************************************************************
C*********************************************************************
      DIMENSION GS(150), R(155),XXS(55), FLNU(55), VG(55), VII(55)
      DATA SLAMIN,SLAMAX,TLAMIN,TLAMAX/0.2, 3.0, 0.05, 6.0/
C      WRITE(IVIDEO,*)'^^^^^^ ENTRA IN MIGRAD'
      IF (NPAR .LE. 0)  RETURN
      ISWTR = ISW(5) - ITAUR
      NPFN = NFCN
      PARN=NPAR
      RHO2 = 10.*APSI
      ROSTOP = 1.0E-5 * APSI
      TRACE=1.
      IFLAG=4
      IF (ISW(3) .EQ. 1)  IFLAG = 2
      FS = AMIN
      IF (ITAUR .LT. 1)  WRITE(ISYSWR,470)  ROSTOP, APSI, VTEST
      GO TO 2
    1 WRITE(ISYSWR,520)
C                                        . . . . STEP SIZES DIRIN . . .
    2 NPARD = NPAR
      DO  3 I= 1, NPAR
      D = 0.02* ABS(DIRIN(I))
      IF (ISW(2) .GE. 1)  D = 0.02* SQRT(ABS(V(I,I))*UP)
      IF (D .LT.  1.0E-8 *ABS(X(I)))  D = 1.0E-8 * X(I)
    3 DIRIN(I) = D
C                                        . . . . . . STARTING GRADIENT
      NTRY = 0
    4 NEGG2 = 0
      DO 10  ID= 1, NPARD
      I = ID + NPAR - NPARD
      D = DIRIN(I)
      XTF = X(I)
      X(I) = XTF + D
C      WRITE(IVIDEO,*)'^^^^ CHIAMA INTOEX ED FCN 1'
      CALL INTOEX(X)
      CALL FCN(NPAR,G,FS1,U,4)
      NFCN = NFCN + 1
      X(I) = XTF - D
C      WRITE(IVIDEO,*)'^^^^ CHIAMA INTOEX ED FCN 2'
      CALL INTOEX(X)
      CALL FCN(NPAR,G,FS2,U,4)
      NFCN = NFCN + 1
      X(I) = XTF
      GS(I) = (FS1-FS2)/(2.0 * D)
      G2(I) = (FS1 + FS2 - 2.0*AMIN) / D**2
      IF (G2(I) .GT. 1.0E-30)  GO TO 10
C                                        . . . SEARCH IF G2 .LE. 0. . .
      WRITE(ISYSWR,520)
      NEGG2 = NEGG2 + 1
      NTRY = NTRY + 1
      IF (NTRY .GT. 4)  GO TO 230
      D = 50.*ABS(DIRIN(I))
      XBEG = XTF
      IF (GS(I) .LT. 0.)  DIRIN(I) = -DIRIN(I)
      KG = 0
      NF = 0
      NS = 0
    5 X(I) = XTF + D
C      WRITE(IVIDEO,*)'^^^^ CHIAMA INTOEX ED FCN 3'
      CALL INTOEX(X)
      CALL FCN(NPAR,G,F,U,4)
      NFCN = NFCN + 1
      IF (F .LE. AMIN)  GO TO 6
C         FAILURE
      IF (KG .EQ. 1)  GO TO 8
      KG = -1
      NF = NF + 1
      D = -0.4*D
      IF (NF .LT. 10)  GO TO 5
      D = 1000.*D
      GO TO 7
C         SUCCESS
    6 XTF = X(I)
      D = 3.0*D
      AMIN = F
      KG = 1
      NS = NS + 1
      IF (NS .LT. 10)  GO TO 5
      IF (AMIN .LT. FS)  GO TO 8
      D = 0.001*D
    7 XTF = XBEG
      G2(I) = 1.0
      NEGG2 = NEGG2 - 1
    8 X(I) = XTF
      DIRIN(I) = 0.1*D
      FS = AMIN
   10 CONTINUE
      IF (NEGG2 .GE. 1)  GO TO 4
      NTRY = 0
      MATGD = 1
C                                        . . . . . . DIAGONAL MATRIX
      IF (ISW(2) .GT. 1)  GO TO 15
   11 NTRY = 1
      MATGD = 0
      DO 13 I= 1, NPAR
      DO 12 J= 1, NPAR
   12 V(I,J) = 0.
   13 V(I,I) = 2.0/G2(I)
C                                        . . . GET SIGMA AND SET UP LOOP
   15 SIGMA = 0.
      DO 18 I= 1, NPAR
      IF (V(I,I) .LE. 0.)  GO TO 11
      RI = 0.
      DO 17 J= 1, NPAR
      XXS(I) = X(I)
   17 RI= RI+ V(I,J) * GS(J)
   18 SIGMA = SIGMA + GS(I) *RI *0.5
      IF (SIGMA .GE. 0.)  GO TO 20
      WRITE(ISYSWR,520)
      IF (NTRY.EQ.0)  GO TO 11
      ISW(2) = 0
      GO TO 230
   20 ISW(2) = 1
      ITER = 0
C      WRITE(IVIDEO,*)'^^^^ CHIAMA INTOEX ED FCN 4'
      CALL INTOEX(X)
      IF (ISWTR .GE. 1)  CALL MPRINT(0,AMIN)
      IF (ISWTR .GE. 2)  CALL MATOUT(0.0D0, 1)
C                                        . . . . .  START MAIN LOOP
   24 CONTINUE
      GDEL = 0.
      DO 30  I=1,NPAR
      RI = 0.
      DO 25 J=1,NPAR
   25 RI = RI + V(I,J) *GS(J)
      DIRIN(I) = -0.5*RI
      GDEL = GDEL + DIRIN(I)*GS(I)
C                                        .LINEAR SEARCH ALONG -VG  . . .
   30 X(I) =XXS(I) + DIRIN(I)
C      WRITE(IVIDEO,*)'^^^^^ CHIAMA INTOEX 5'
      CALL INTOEX(X)
C      WRITE(IVIDEO,*) '^^^^^ RIENTRA DA INTOEX 5'
C      WRITE(IVIDEO,*)'^^^^^ CHIAMA FCN 5'
      CALL FCN (NPAR, G, F, U, 4)
C      WRITE(IVIDEO,*)'^^^^^ RIENTRA DA FCN 5'
      NFCN=NFCN+1
C                                        . QUADR INTERP USING SLOPE GDEL
      DENOM = 2.0*(F-AMIN-GDEL)
      IF (DENOM .LE. 0.)  GO TO 35
C      WRITE(IVIDEO,*)'PASSA SOTTO IL GOTO 35 (1)'
      SLAM = -GDEL/DENOM
      IF (SLAM .GT. SLAMAX)  GO TO 35
C      WRITE(IVIDEO,*)'PASSA SOTTO IL GOTO 35 (2)'
      IF (SLAM .LT. SLAMIN)  SLAM=SLAMIN
      GO TO 40
   35 SLAM = SLAMAX
   40 IF (ABS(SLAM-1.0D0) .LT. 0.1)  GO TO 70
C      WRITE(IVIDEO,*)'PASSA SOTTO GO TO 70'
      DO 45 I= 1, NPAR
   45 X(I) =XXS(I) + SLAM*DIRIN(I)
C      WRITE(IVIDEO,*)'^^^^^ CHIAMA INTOEX ED FCN 6'
      CALL INTOEX(X)
      CALL FCN(NPAR,G,F2,U,4)
      NFCN = NFCN + 1
C                                        . QUADR INTERP USING 3 POINTS
      AA = FS/SLAM
      BB = F/(1.0D0-SLAM)
      CC = F2/ (SLAM*(SLAM-1.0D0))
      DENOM = 2.0*(AA+BB+CC)
      IF (DENOM .LE. 0.)  GO TO 48
      TLAM = (AA*(SLAM+1.0D0) + BB*SLAM + CC)/DENOM
      IF (TLAM .GT. TLAMAX)  GO TO 48
      IF (TLAM .LT. TLAMIN)  TLAM=TLAMIN
      GO TO 50
   48 TLAM = TLAMAX
   50 CONTINUE
      DO 51 I= 1, NPAR
   51 X(I) = XXS(I)+TLAM*DIRIN(I)
C      WRITE(IVIDEO,*)'^^^^^ CHIAMA INTOEX ED FCN 6'
      CALL INTOEX(X)
      CALL FCN(NPAR,G,F3,U,4)
      NFCN = NFCN + 1
      IF (F.GE.AMIN .AND. F2.GE.AMIN .AND. F3.GE.AMIN) GO TO 200
      IF (F .LT. F2 .AND. F .LT. F3)  GO TO 61
      IF (F2 .LT. F3)  GO TO 58
   55 F = F3
      SLAM = TLAM
      GO TO 65
   58 F = F2
      GO TO 65
   61 SLAM = 1.0
   65 DO 67 I= 1, NPAR
      DIRIN(I) = DIRIN(I)*SLAM
   67 X(I) = XXS(I) + DIRIN(I)
   70 CONTINUE
      AMIN = F
C      WRITE(IVIDEO,*) 'CALCOLATO AMIN', AMIN
       ISW(2) = 2
      IF (SIGMA+FS-AMIN .LT. ROSTOP)  GO TO 170
C      WRITE(IVIDEO,*)'PASSA SOTTO IL GO TO 170 1'
      IF (SIGMA+RHO2+FS-AMIN .GT. APSI)  GO TO 75
C      WRITE(IVIDEO,*)'PASSA SOTTO IL GO TO 75 1'
      IF (TRACE .LT. VTEST)  GO TO 170
C      WRITE(IVIDEO,*)'PASSA SOTTO IL GO TO 170 2'
   75 CONTINUE
      IF (NFCN-NPFN .GE. NFCNMX)  GO TO 190
C      WRITE(IVIDEO,*)'PASSA SOTTO IL GO TO 190'
      ITER = ITER + 1
      IF (ISWTR.GE. 3 .OR.(ISWTR.EQ. 2 .AND. MOD(ITER,10) .EQ.1))
     *      CALL MPRINT(0,AMIN)
C                                        . . . GET GRADIENT AND SIGMA .
      IF (ISW(3) .NE. 1)  GO TO 80
C      WRITE(IVIDEO,*)'PASSA SOTTO IL GO TO 80'
C      WRITE(IVIDEO,*)'^^^^^ CHIAMA INTOEX ED FCN 7'
      CALL INTOEX(X)
      CALL FCN(NPAR,G,AMIN,U,IFLAG)
      NFCN = NFCN + 1
   80 CALL DERIVE(G,G2)
      RHO2 = SIGMA
      SIGMA = 0.
      GVG = 0.
      DELGAM = 0.
      DO 100 I= 1, NPAR
      RI = 0.
      VGI = 0.
      DO 90 J= 1, NPAR
      VGI = VGI + V(I,J)*(G(J)-GS(J))
   90 RI = RI + V(I,J) *G (J)
      R(I) = RI * 0.5
      VG(I) = VGI*0.5
      GAMI = G(I) - GS(I)
      GVG = GVG + GAMI*VG(I)
      DELGAM = DELGAM + DIRIN(I)*GAMI
  100 SIGMA = SIGMA + G(I)*R(I)
      IF (SIGMA .LT. 0.)  GO TO 1
      IF (GVG .LE. 0.)  GO TO 105
      IF (DELGAM .LE. 0.)  GO TO 105
      GO TO 107
  105 IF (SIGMA .LT. 0.1*ROSTOP)  GO TO 170
      GO TO 1
  107 CONTINUE
C                                        .  UPDATE COVARIANCE MATRIX
      TRACE=0.
      DO 120 I= 1, NPAR
      VII(I) = V(I,I)
      DO  120  J=1,NPAR
      D = DIRIN(I)*DIRIN(J)/DELGAM - VG(I)*VG(J)/GVG
  120 V(I,J) = V(I,J) + 2.0*D
      IF (DELGAM .LE. GVG)  GO TO 135
      DO 125 I= 1, NPAR
  125 FLNU(I) = DIRIN(I)/DELGAM - VG(I)/GVG
      DO 130 I= 1, NPAR
      DO 130 J= 1, NPAR
  130 V(I,J) = V(I,J) + 2.0*GVG*FLNU(I)*FLNU(J)
  135 CONTINUE
      DO 140 I= 1, NPAR
      XXS(I) = X(I)
      GS(I) = G(I)
  140 TRACE = TRACE + ((V(I,I)-VII(I))/(V(I,I)+VII(I)))**2
      TRACE = SQRT(TRACE/PARN)
      IF (ISWTR .GE. 4)  CALL MATOUT(TRACE, 0)
      FS = F
      GO TO 24
C                                        . . . . .  END MAIN LOOP
  170 WRITE (ISYSWR,500)
      ISW(2) = 3
      IF(ISWTR .GE. 0)  CALL MPRINT(1-ITAUR,AMIN)
C      WRITE(IVIDEO,*) 'RIENTRA DA MPRINT'
      ISWTR = ISWTR - 3*ITAUR
      IF (ISWTR .GT. 0)  CALL MATOUT(TRACE,1)
C      WRITE(IVIDEO,*) 'RIENTRA DA MATOUT'
      IF (ITAUR .GT. 0)  GO TO 435
C      WRITE(IVIDEO,*)'PASSA SOTTO IL GO TO 435 1'
      IF (MATGD .GT. 0)  GO TO 435
C      WRITE(IVIDEO,*)'PASSA SOTTO IL GO TO 435 2'
      NPARGD = NPAR*(NPAR+5)/2
      IF (NFCN-NPFN .GE. NPARGD)  GO TO 435
C      WRITE(IVIDEO,*)'PASSA SOTTO GO TO 435 3'
      WRITE(ISYSWR,180)
  180 FORMAT (  55H COVARIANCE MATRIX INACCURATE.  MINUIT WILL RECALCULA
     1TE)
C      WRITE(IVIDEO,*)'^^^^ CHIAMA HESSE'
      CALL HESSE
