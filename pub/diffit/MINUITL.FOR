      CALL MATOUT(0.0D0, 1)
      IF (ISW(2) .GE. 2)  ISW(2) = 3
      GO TO 435
  190 ISW(1) = 1
      GO TO 230
  200 WRITE(ISYSWR,650)
      DO 210 I= 1, NPAR
  210 X(I) = XXS(I)
