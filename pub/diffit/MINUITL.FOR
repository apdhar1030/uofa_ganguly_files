      DO 410 IBK= 1, NX
      ALINE(IBK) = ABLANK
      IF (MOD(IBK,10) .EQ. 1)  ALINE(IBK) = SLASH
  410 CONTINUE
      WRITE (ISYSWR, 1007)       (ALINE(IW),IW=1,NX)
C
      DO 430 IBK= 1, 12
  430 XVALUS(IBK) = XMIN + FLOAT(IBK-1)*10.*BWIDX
      ITEN = (NX+9) / 10
      WRITE (ISYSWR,1004)  (XVALUS(IBK), IBK=1,ITEN)
