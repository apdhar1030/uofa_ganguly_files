      SUBROUTINE  DERIVE(GG,GG2)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
CC        CALCULATES THE FIRST DERIVATIVES OF FCN (GG),
CC        EITHER BY FINITE DIFFERENCES OR BY TRANSFORMING THE USER-
CC        SUPPLIED DERIVATIVES TO INTERNAL COORDINATES,
CC        ACCORDING TO WHETHER ISW(3) IS ZERO OR ONE.
CC        IF ISW(3) = 0, AN ERROR ESTIMATE GG2  IS AVAILABLE
CC
C********************************************************************
C********** C O M M O N     PER  MINUITL ****************************
C*********************************************************************
C********************************************************************
C********** C O M M O N     PER  MINUITL ****************************
      COMMON
     1/MINERR/ ERP(150)     ,ERN(150)
     2/PARINT/ X(55)       ,XT(55)     ,DIRIN(55)  ,MAXINT     ,NPAR
     3/PAREXT/ U(150)       ,FNAM(150)    ,WERR(150)   ,MAXEXT     ,NU
     4/LIMITS/ ALIM(150),BLIM(150),LCODE(150),LCORSP(150) ,LIMSET
     5/VARIAN/ V(55,55)
     7 /FIX/    XS(55),   XTS(55),   WTS(55),   IPFIX(55),   NPFIX
     C/CASC/ JH, JL, Y(56)
     F/DERIVA/ G(150)       ,G2(150)
     G/SIMVEC/ P(55,56)    ,PSTAR(55)  ,PSTST(55)  ,PBAR(55)   ,PRHO(55)
     J/VARIAT/ VT(55,55)
      COMMON
     6/UNIT/  ISYSRD,ISYSWR,ISYSPU,IVIDEO
     8/TITLE/ TITLE(13),DATE(2),ISW(7),NBLOCK
     9/CONVER/ EPSI,APSI,VTEST,NSTEPQ,NFCN,NFCNMX
     A/CARD/ CWORD,CWORD2,CWORD3,WORD7(7)
     B/MINIMA/ AMIN,UP,NEWMIN,ITAUR,SIGMA
C*********************************************************************
C*********************************************************************
      DIMENSION GG(30),GG2(15),GY(150)
      DATA EPSMAC/1.0E-5/
      IF (ISW(3) .EQ. 1)  GO TO 100
      IFLAG = 4
      DO 46  I=1,NPAR
      EPS = 0.1 * ABS(DIRIN(I))
      IF (ISW(2) .GE. 1) EPS = EPS + 0.005*SQRT(V(I,I)*UP)
      IF (EPS .LT. EPSMAC*ABS(X(I)))  EPS= EPSMAC*X(I)
      XTF = X(I)
      X(I) = XTF + EPS
      CALL INTOEX(X)
      CALL FCN(NPAR,GY,FS1,U ,IFLAG)
      NFCN=NFCN+1
      X(I) = XTF - EPS
      CALL INTOEX(X)
      CALL FCN(NPAR,GY,FS2,U,IFLAG)
      NFCN=NFCN+1
C                                        . . . . . . .  FIRST DERIVATIVE
      GG(I)= (FS1-FS2)/(2.0*EPS)
C                                        . . . ERROR ON FIRST DERIVATIVE
      GG2(I)= (FS1+FS2-2.0*AMIN)/(2.0*EPS)
      X(I) = XTF
   46 CONTINUE
      CALL INTOEX(X)
      GO TO 200
C                                        .  DERIVATIVES CALC BY FCN
  100 DO 150 I= 1, NU
      LC=LCORSP(I)
      IF (LC .LT. 1)  GO TO 150
      IF (LCODE(I) .GT. 1)  GO TO 120
      GG(LC)=GG(I)
      GO TO 150
  120 DD = (BLIM(I)-ALIM(I))*0.5 *COS(X(LC))
      GG(LC)=GG(I)*DD
  150 CONTINUE
  200 RETURN
      END
