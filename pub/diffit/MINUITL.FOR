C*********************************************************************
C*********************************************************************
      DIMENSION YY(15),GY(150)
      DATA DFWANT,DFZERO,DFMIN,DFMAX/ 0.01, 0.00000001, 0.001, 0.1 /
      WRITE (ISYSWR, 500)
      IFLAG = 4
      NPFN = NFCN
      NPARD = NPAR
C                                        . . . . . . DIAGONAL ELEMENTS .
      MDIAG = 0
      DO 100 ID= 1, NPARD
      I = ID + NPAR - NPARD
      D = 0.02* ABS(DIRIN(I))
