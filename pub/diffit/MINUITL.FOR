      SUBROUTINE VERMIN(A,L,M,N,IFAIL)
C      OVERLAY OVVER
CC        INVERTS A SYMMETRIC MATRIX.   MATRIX IS FIRST SCALED TO
CC        HAVE ALL ONES ON THE DIAGONAL (EQUIVALENT TO CHANGE OF UNITS)
CC        BUT NO PIVOTING IS DONE SINCE MATRIX IS POSITIVE-DEFINITE.
CC
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C********************************************************************
C********** C O M M O N     PER  MINUITL ****************************
C*********************************************************************
C********************************************************************
C********** C O M M O N     PER  MINUITL ****************************
      COMMON
     1/MINERR/ ERP(150)     ,ERN(150)
     2/PARINT/ X(55)       ,XT(55)     ,DIRIN(55)  ,MAXINT     ,NPAR
     3/PAREXT/ U(150)       ,FNAM(150)    ,WERR(150)   ,MAXEXT     ,NU
     4/LIMITS/ ALIM(150),BLIM(150),LCODE(150),LCORSP(150) ,LIMSET
     5/VARIAN/ V(55,55)
     7 /FIX/    XS(55),   XTS(55),   WTS(55),   IPFIX(55),   NPFIX
     C/CASC/ JH, JL, Y(56)
     F/DERIVA/ G(150)       ,G2(150)
     G/SIMVEC/ P(55,56)    ,PSTAR(55)  ,PSTST(55)  ,PBAR(55)   ,PRHO(55)
     J/VARIAT/ VT(55,55)
      COMMON
     6/UNIT/  ISYSRD,ISYSWR,ISYSPU,IVIDEO
     8/TITLE/ TITLE(13),DATE(2),ISW(7),NBLOCK
     9/CONVER/ EPSI,APSI,VTEST,NSTEPQ,NFCN,NFCNMX
     A/CARD/ CWORD,CWORD2,CWORD3,WORD7(7)
     B/MINIMA/ AMIN,UP,NEWMIN,ITAUR,SIGMA
C*********************************************************************
C*********************************************************************
      DIMENSION A(L,M) , PP(55) , Q(55) , S(55)
      IFAIL=0
      IF (N .LT. 1)  GO TO 100
      IF (N .GT. MAXINT)  GO TO 100
C                   SCALE MATRIX BY SQRT OF DIAG ELEMENTS
      DO 8  I=1,N
      SI = A(I,I)
      IF (SI) 100,100,8
    8 S(I) = 1.0/SQRT(SI)
      DO 20 I= 1, N
      DO 20 J= 1, N
   20 A(I,J) = A(I,J) *S(I)*S(J)
C                                        . . . START MAIN LOOP . . . .
      DO 65 I=1,N
      K = I
C                   PREPARATION FOR ELIMINATION STEP1
      Q(K)=1./A(K,K)
      PP(K) = 1.0
      A(K,K)=0.0
      KP1=K+1
      KM1=K-1
      IF(KM1)100,50,40
   40 DO 49 J=1,KM1
      PP(J)=A(J,K)
      Q(J)=A(J,K)*Q(K)
   49 A(J,K)=0.
   50 IF(K-N)51,60,100
   51 DO 59 J=KP1,N
      PP(J)=A(K,J)
      Q(J)=-A(K,J)*Q(K)
   59 A(K,J)=0.0
C                   ELIMINATION PROPER
   60 DO 65 J=1,N
      DO 65 K=J,N
   65 A(J,K)=A(J,K)+PP(J)*Q(K)
C                   ELEMENTS OF LEFT DIAGONAL AND UNSCALING
      DO 70 J= 1, N
      DO 70 K= 1, J
      A(K,J) = A(K,J) *S(K)*S(J)
   70 A(J,K) = A(K,J)
      RETURN
C                   FAILURE RETURN
  100 IFAIL=1
      RETURN
      END
